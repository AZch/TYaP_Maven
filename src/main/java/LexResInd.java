public class LexResInd {
    private char[] lex;
    private int codeRes;
    private int indexLex;

    public char[] getLex() {
        return lex;
    }

    public int getCodeRes() {
        return codeRes;
    }

    public int getIndexLex() {
        return indexLex;
    }

    public void setCodeRes(int codeRes) {
        this.codeRes = codeRes;
    }

    public void setLex(char[] lex) {
        this.lex = lex;
    }

    public void setIndexLex(int indexLex) {
        this.indexLex = indexLex;
    }
}
